package loop;

public class Trapeziuminver {
	public static void main(String[] args) {
		for(int i=3;i<=6;i++) {    
			for(int b=1;b<=i;b++) 
			{System.out.print(" ");}
			for(int a=6;a>=i-2;a--) 
				System.out.print(" *");
			System.out.println();
			}
		
	}}
		
	

	
		
		
		
		
		
	


